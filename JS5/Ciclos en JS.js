//CICLO WHILE

let  timer = 0;

while (timer < 3){
    console.log(timer);
    timer++;
}
console.log("End of timer");

// CICLO DO WHILE

let count = 0

do{
    console.log(count);
    count++;
}while(count < 6);
console.log("End of count");

// CICLO FOR
/*Tiene como primera parte donde declaro la variable, 
la segunda donde pongo la condicion y la tercera donde
incremento o decremento */

for( let recount = 0; recount < 3; recount++){
    console.log(recount);
}
console.log( "end of recount");