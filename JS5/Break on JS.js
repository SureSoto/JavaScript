// Imprimir solo numeros pares o el primer numero par
for(let timer= 0; timer <= 10; timer++ ){
    if( timer % 2 == 0){
        console.log(timer);
        break;
    } 
}
console.log("end of timer");
/* En este ejercicio se utilizo la palabra break para que solo me sacara el primer
numero  par, si se quita break se me ejecutara el programa con todos los numeros pares. */